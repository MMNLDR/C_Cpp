//测试三子棋游戏
#include <stdio.h> 
#define _CRT_SUCURE_NO_WARNINGS
#define ROW 3//定义行数 
#define COL 3//定义列数 

void menu()
{
	printf("*****************************\n");
	printf("****   1.play   0.exit   ****\n");
	printf("*****************************\n");
}

//初始化生成空格 
void InitBoard(char board[ROW][COL], int row, int col)//声明
{
	int i = 0;
		int j = 0;
		for(i=0; i<row; i++)
		{
			for(j=0; j<col; j++)
			{
				board[i][j] = ' ';	
			}
		}
}
 
void DisplayBoard(char board[ROW][COL], int row, int col)
{
	int i =0;
	for(i=0; i<row; i++)
	{
		//1.打印一行的数据
		int j = 0;
		for(j=0; j<col; j++)
		{
			printf(" %c ", board[i][j]);
			if(j<col-1)
				printf("|");
		}
		printf("\n");
		//2.打印分割行
		if(i<row-1)
		{
			for(j=0; j<col; j++)
			{
				printf("---");
				if(j<col-1)
				printf("|");
			}	
			printf("\n");
		}
	}
}

//游戏算法的实现 
void game()
{
	//数组-存放走出的棋盘信息
	char board[ROW][COL] = {0};//全部空格
	//初始化棋盘
	InitBoard(board, ROW, COL);
	//打印棋盘
	DisplayBoard(board, ROW, COL);
	
	 
}

void test()
{
	int input = 0;
	do
	{
		menu();
		printf("请选择；");
		scanf("%d", &input);
		switch(input)
		{
			case 1:
				printf("三子棋\n");
				break;
			case 0:
				printf("退出游戏\n");
				break;
			default:
				printf("选择错误，请重新选择！\n");
				break;	
		} 	
	}while(input);
}


int main()
{
	 test();
		
	return 0;
} 
