#include <stdio.h>

/*整形提升 - 在计算过程中发现有些操作数达不到整形
的大小，就会发生整形提升变成一个整形再参与整形运算 */ 

/*在整形提升时，如果是有符号数，则按最高位将前面补齐32位
如果是无符号数，则将前面全部补0补齐32位*/

int main()
{
	char a = 3;
	//00000000000000000000000000000011
	//00000011 - a
	
	char b = 127;
	//00000000000000000000000001111111
	//01111111 - b
	
	//a和b如何相加
	//00000000000000000000000000000011
	//00000000000000000000000001111111
	//00000000000000000000000010000010
	 
	char c = a + b;
	//10000010 - c
	//11111111111111111111111110000001 - 补码 
	//11111111111111111111111110000001 - 反码 
	//10000000000000000000000001111110 - 原码 
	printf("%d\n", c);
	
	return 0;
} 
